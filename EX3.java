//EX3 ON LOCAL VARIABLE DECLARATION
public class EX3								//MAIN CLASS
{
	public static void main(String arg[])
	{
		boolean a=true;							//FOR DECLARING BOOOLEAN DATATYPE
		int b=150000;							//FOR DECLARING INTEGER DATATYPE
		long c=4000000000L;						//FOR DECLARING LONG DATATYPE
		float d=10.72f;							//FOR DECLARING FLOAT DATATYPE
		double e=10000476;						//FOR DECLARING DOUBLE DATATYPE
		char f='y';								//FOR DECLARING CHAR DATATYPE
		String y="PRASANTH";					//FOR DECLARING STRING DATATYPE
		System.out.println("A value is"+a);
		System.out.println("Hi"+y);
		System.out.println("Hello\n"+y);
	}
}