//EX2 ON SEQUENCE CHARACTERS
public class EX2								//MAIN CLASS
{
	public static void main(String arg[])
	{
		System.out.println("RAMANA\n\n\n\n");	//\n USED FOR JUMPING INTO NEXT LINE SIMILAR TO ENTER
		System.out.println("USHA\tRANI");		//\t USED TO GIVE TAB SPACE
		System.out.println("\"PRASANTH\"");		//\" USED TO GIVE QUATATIONS TO MESSAGE
		System.out.println("BHAVYA\r N");		//\r USED TO RETURN TO THE STARTING OF LINE SIMILAR TO HOME
		System.out.println("\0\1\2\3\4\5\6");	//\0 SPACE \1 SMILEY-1 \2 SMILEY-2 \3\4\5\6 CARDS SYMBOLS
		System.out.println("PRASANTHI\b");		//\b BACK SPACE PRINTLN CURSOR IN NEXT LINE BACKSPACE BRINGS IT BACK TO SAME LINE
	}
}