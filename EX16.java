//EX16 ON DIFFERENT TYPE OF NUMBER SYSTEMS
public class EX16
{
	public static void main(String arg[])
	{
		int a=123;					//DECIMAL VALUE
		int b=0b101;				//BINARY VALUE
		int c=00126;				//OCTADECIMAL VALUE
		int d=0x12c;				//HEXADECIMAL VALUE
		System.out.println("DEC:"+a);
		System.out.println("BINARY:"+b);
		System.out.println("OCTA:"+c);
		System.out.println("HEXA:"+d);
	}
}
